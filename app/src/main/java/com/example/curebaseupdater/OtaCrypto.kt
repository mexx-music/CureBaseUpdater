    /** zlib wrapper (Deflater ohne nowrap) – passt zu mz_inflateInit (WindowBits 15) */
    fun zlibCompress(input: ByteArray, level: Int = Deflater.BEST_COMPRESSION): ByteArray {
        val bos = ByteArrayOutputStream()
        val deflater = Deflater(level, /*nowrap=*/false) // false => zlib header/trailer
        DeflaterOutputStream(bos, deflater).use { it.write(input) }
        return bos.toByteArray()
    }

    /** AES-CTR encrypt (AES-256). Cipher behält Counter-State über update() – wichtig! */
    fun aesCtrEncryptStream(key32: ByteArray, iv16: ByteArray): Cipher {
        require(key32.size == 32) { "OTA key must be 32 bytes (AES-256)" }
        require(iv16.size == 16) { "OTA iv must be 16 bytes" }
        val cipher = Cipher.getInstance("AES/CTR/NoPadding")
        cipher.init(
            Cipher.ENCRYPT_MODE,
            SecretKeySpec(key32, "AES"),
            IvParameterSpec(iv16)
        )
        return cipher
    }
